<script lang="ts">
import { defineComponent, onMounted, ref, watch } from "vue";
import SelectionArea from "@simonwep/selection-js";
export default defineComponent({
  name: "Home",
  setup() {
    let total_select = ref(0);
    let isSmall = window.matchMedia("(max-width: 600px)").matches;
    let touch_dev = 0;
    const touched_div = () => (touch_dev = 1);
    const elem = [];
    if (!isSmall) {
      for (let i = 0; i < 191; i++) elem[i] = ".";
    } else {
      for (let i = 0; i < 2240; i++) elem[i] = ".";
    }
    onMounted(() => {
      const selection = new SelectionArea({
        class: "selection-area",
        selectables: [
          ".cont_pixe > div >  div:empty",
          ".small_cont > div:empty",
        ],
        boundaries: [".cont_pixe", "#app"],
        startThreshold: 4,
        allowTouch: false,
      })
        .on("start", ({ store, event }) => {
          event?.stopPropagation();
          // Remove class if the user isn't pressing the control key or ⌘ key
          if (touch_dev === 0 && event !== null) {
            // Unselect all elements
            for (const el of store.stored) {
              el.classList.remove("selected");
            }
            // Clear previous selection
            selection.clearSelection();
          }
        })
        .on(
          "move",
          ({
            store: {
              changed: { added, removed },
            },
            event,
            store,
          }) => {
            // Add a custom class to the elements that where selected.
            if (event === null) {
              if (added[0] !== undefined) {
                added[0].classList.add("selected");
                total_select.value++;
              } else if (removed[0] !== undefined) {
                removed[0].classList.remove("selected");
                total_select.value--;
              }
            } else if (event.constructor.name !== "TouchEvent") {
              for (const el of added) {
                el.classList.add("selected");
                total_select.value++;
              }
              // Remove the class from elements that where removed
              // since the last selection
              for (const el of removed) {
                el.classList.remove("selected");
                total_select.value--;
              }
            }
          }
        )
        .on("stop", () => {
          selection.keepSelection();
          total_select.value = selection.getSelection().length;
        });
    });
    watch(total_select, (new_val, _) => {
      const don_but = document.querySelector<HTMLElement>(
        ".bar-menu #Donate-button .Group_Donate .Group_EffectDonate"
      );
      let is_ass = false;
      if (don_but !== null && !is_ass && new_val !== 0) {
        don_but.classList.add("selected_to_pay");
        is_ass = true;
      } else if (don_but !== null && new_val == 0) {
        don_but.classList.remove("selected_to_pay");
        is_ass = false;
      }
    });
    return { elem, total_select, isSmall, touched_div };
  },
});
</script>

